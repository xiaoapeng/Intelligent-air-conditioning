int fputc(int ch, FILE *p)
{
	while (SET != USART_GetFlagStatus(USART1, USART_FLAG_TXE));
	USART_SendData(USART1, ch);

	return ch;
}
int fputc_u3(int ch, FILE *p)
{
	while (SET != USART_GetFlagStatus(USART2, USART_FLAG_TXE));
	USART_SendData(USART2, ch);
	return ch;
} 

void send_usart3(char *a,u16 size)
{
	int i=0;
	for(i=0;i<size;i++)
		fputc_u3(a[i],(FILE*)-1);
}


int put_c(int ch)
{
	fputc(ch,(FILE*)-1);
}



